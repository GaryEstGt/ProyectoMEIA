    public boolean VerificarCampos(){
        if(txtNombre.getText().isEmpty())
            return false;
        return !txtDesc.getText().isEmpty();
    }
    public boolean VerificarLlaves(Lista list){
       boolean Encontrado=false;
       if(SecuencialLista.obtenerDescriptorLista(2).getNumRegistros()!=0){
         for(Lista elemento:SecuencialLista.obtenerListas(1)){
           if(elemento.getNombreLista().equals(list.getNombreLista()) && elemento.getUsuario().equals(list.getUsuario())){
               Encontrado = true;
           }
          }  
       }
       if(SecuencialLista.obtenerDescriptorLista(1).getNumRegistros()!=0){
         for(Lista elemento:SecuencialLista.obtenerListas(2)){
           if(elemento.getNombreLista().equals(list.getNombreLista()) && elemento.getUsuario().equals(list.getUsuario())){
               Encontrado = true;
           }
         }         
       }
       return Encontrado;
    }

