    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        Date fechaActual = new Date();
        if(VerificarCampos()){
            Lista nuevaLista=new Lista(txtNombre.getText(),ProyectoMEIA.usuarioEnUso.getUsuario(),txtDesc.getText(),0,fechaActual.toString(),1);
            if(SecuencialLista.obtenerDescriptorLista(2).getNumRegistros()< SecuencialLista.obtenerDescriptorLista(2).getMaxReorganizacion()){
                if(!VerificarLlaves(nuevaLista)){
                    String textoAnterior="";
                    boolean PrimerIngreso=false;
                    if(SecuencialLista.obtenerDescriptorLista(2).numRegistros!=0){
                        textoAnterior=Lector.Obtener("C:/MEIA/bitacora_lista.txt");
                        PrimerIngreso=true;
                    }
                     Escritor.Escribir("C:/MEIA/bitacora_lista.txt", textoAnterior+nuevaLista.toString());  
                       DescriptorLista des = SecuencialLista.obtenerDescriptorLista(2);
                            
                            des.setNumRegistros(des.getNumRegistros()+1);
                            des.setRegistrosActivos(des.getRegistrosActivos()+1);                            
                            des.setUsuarioModificacion(nuevaLista.getUsuario());
                            des.setFechaModificacion(fechaActual.toString());
                            if(PrimerIngreso==true){
                                des.setFechaCreacion(fechaActual.toString());
                                des.setUsuarioCreacion(ProyectoMEIA.usuarioEnUso.getUsuario()); 
                            }
                            SecuencialLista.rellenarDescriptorLista(des, 2);
                }
                else{
                    JOptionPane.showMessageDialog(null,"Lista ya Existente");
                    txtNombre.setText("");
                }
            }
            else{
                try {
                    SecuencialLista.LlenarListasMaestro();
                    if(!VerificarLlaves(nuevaLista)){
                    String textoAnterior="";
                    if(SecuencialLista.obtenerDescriptorLista(2).numRegistros!=0){
                        textoAnterior=Lector.Obtener("C:/MEIA/bitacora_lista.txt");
                    }
                     Escritor.Escribir("C:/MEIA/bitacora_lista.txt", textoAnterior+nuevaLista.toString());  
                       DescriptorLista des = SecuencialLista.obtenerDescriptorLista(2);
                            
                            des.setNumRegistros(des.getNumRegistros()+1);
                            des.setRegistrosActivos(des.getRegistrosActivos()+1);                            
                            des.setUsuarioModificacion(nuevaLista.getUsuario());
                            des.setFechaModificacion(fechaActual.toString());

                        
                            SecuencialLista.rellenarDescriptorLista(des, 2);
                }
                else{
                    JOptionPane.showMessageDialog(null,"Lista ya Existente");
                    txtNombre.setText("");
                }
                } catch (IOException ex) {
                    Logger.getLogger(ManejodeListas.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Los campos deben estar lleno");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

